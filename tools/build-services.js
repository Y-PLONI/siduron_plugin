/* build-services.js — ports the MAIN prayer services (shacharit / mincha /
 * arvit, with musaf + shabbat folded in via runtime conditions) from the Tfilon
 * corpus into data/services-data.js, for ALL three nuschaot.
 *
 * Why Tfilon and not smart-siddur: smart-siddur's non-Ashkenaz (עדות המזרח /
 * ספרד) data is a partial, contaminated port — many segments carry Ashkenaz or
 * wrong-service text, and pieces are missing. Tfilon ships complete, correct
 * per-nusach services (a_/s_/em_ prefixes). We parse them with the same engine
 * as the תוספות (tools/lib/tfilon.js) and evaluate the [cond] tags at runtime
 * (js/services.js) against the day's flags (js/calendar.js).
 *
 * Run:  node tools/build-services.js
 * Out:  data/services-data.js   (window.SIDURON_SERVICES)
 */
'use strict';
const fs = require('fs');
const path = require('path');
const { createParser, isForbidden } = require('./lib/tfilon');

const ROOT = path.resolve(__dirname, '..');
const TF = path.join(ROOT, 'tfilon.tfilon_38_1734630-1', 'assets');
const OUT = path.join(ROOT, 'data', 'services-data.js');
const tfilon = createParser(TF);

const NUSACHIM = { ashkenaz: 'a', sfard: 's', edot_mizrach: 'em' };
const SERVICES = ['shacharit', 'mincha', 'arvit'];   // musaf + shabbat fold in via conditions

// Content policy: drop the State-of-Israel / IDF day additions wholesale —
// every segment gated on the atzmaut/zikaron conditions, plus an isForbidden
// backstop on the text itself (mirrors tools/build-extras.js).
const DROP_CONDS = ['atzmaut', 'zikaron'];
function isDropped(seg) {
  if ((seg.cond || []).some(c => DROP_CONDS.includes(c))) return true;
  if (isForbidden(seg.text)) return true;
  return false;
}

const out = {};
const stats = [];
let missing = 0;

for (const [nusach, prefix] of Object.entries(NUSACHIM)) {
  out[nusach] = {};
  for (const svc of SERVICES) {
    const entry = prefix + '_' + svc;
    if (!fs.existsSync(path.join(TF, entry))) { stats.push(`  ! no entry file: ${entry}`); continue; }
    let segs = tfilon.parseEntry(entry);
    const miss = segs.filter(s => s.kind === 'missing');
    miss.forEach(m => { console.log('  ! missing include in', entry + ':', m.text); missing++; });
    segs = segs.filter(s => s.kind !== 'missing' && !isDropped(s));
    out[nusach][svc] = segs;
    stats.push(`  ${nusach.padEnd(13)} ${svc.padEnd(10)} (${entry.padEnd(13)}) segs=${segs.length}`);
  }
}

fs.mkdirSync(path.dirname(OUT), { recursive: true });
const header = '/* services-data.js — GENERATED by tools/build-services.js. Do not edit by hand.\n' +
  ' * Main services (שחרית/מנחה/ערבית + מוסף/שבת via conditions) ported from the\n' +
  ' * Tfilon corpus for all nuschaot. Conditions evaluated at runtime in js/services.js.\n */\n';
fs.writeFileSync(OUT, header + 'window.SIDURON_SERVICES = ' + JSON.stringify(out) + ';\n', 'utf8');

const bytes = fs.statSync(OUT).size;
console.log('Wrote', path.relative(ROOT, OUT), '(' + (bytes / 1024 / 1024).toFixed(2) + ' MB)');
stats.forEach(s => console.log(s));
if (missing) console.log('TOTAL missing includes:', missing);
